package com.tinderbooksproject.model;

public class InformacionUsuarios {
	private String pDescripcion;
	private String pEdad;
	private String pNacionalidad;
	private String pCiudad;
	private String pAcademica;
	
	public InformacionUsuarios() {
		
	}
	
	public String getDescripcion() {
		return pDescripcion;
	}
	
	public void setDescripcion(String pDescripcion) {
		this.pDescripcion = pDescripcion;
	}
	
	public String getEdad() {
		return pEdad;
	}
	
	public void setEdad(String pEdad) {
		this.pEdad = pEdad;
	}
	
	public String getNacionalidad() {
		return pNacionalidad;
	}
	
	public void setNacionalidad(String pNacionalidad) {
		this.pNacionalidad = pNacionalidad;
	}
	
	public String getCiudad() {
		return pCiudad;
	}
	
	public void setCiudad(String pCiudad) {
		this.pCiudad = pCiudad;
	}
	
	public String getAcademica() {
		return pAcademica;
	}
	
	public void setAcademica(String pAcademica) {
		this.pAcademica = pAcademica;
	}
	
}
